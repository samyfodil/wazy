func BenchmarkRedundancyHeavyExec(b *testing.B) {
	ctx := context.Background()
	r := wazy.NewRuntimeWithConfig(ctx, wazy.NewRuntimeConfigCompiler())
	defer r.Close(ctx)
	compiled, err := r.CompileModule(ctx, redundancyHeavyWasm)
	if err != nil {
		b.Fatal(err)
	}
	mod, err := r.InstantiateModule(ctx, compiled, wazy.NewModuleConfig().WithName("m"))
	if err != nil {
		b.Fatal(err)
	}
	work := mod.ExportedFunction("work")
	stack := make([]uint64, 1)
	b.ResetTimer()
	for i := 0; i < b.N; i++ {
		stack[0] = 4000
		if err := work.CallWithStack(ctx, stack); err != nil {
			b.Fatal(err)
		}
	}
}
